package loop;

public class For_loop4 {
	
	public static void main(String[] args) {
		
	

	int i = 10;
	int j = 10;
	int k = 20;
	
	
	for (i = 1;i<=j && i<=k; i++,j--,k--) {
		
		System.out.println("i = "+ i + " : j = " + j + " : k = "+ k);
		
	}
	
	System.out.println();
	System.out.println("i = "+ i + " : j = " + j + " : k = "+ k);
	
	
	
	
	
	
	
	



}}
